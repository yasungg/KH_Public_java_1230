package 진수표현;

public class DecType {
    public static void main(String[] args) {
        int var1 = 0b1010; // 2진수
        int var2 = 0207; // 8진수. 앞에 0을 붙인다.
        int var3 = 365;
        int var4 = 0xBA;

        System.out.println("var1 = " + var1);
        System.out.println("var2 = " + var2);
        System.out.println("var3 = " + var3);
        System.out.println("var4 - " + var4);


    }

}
